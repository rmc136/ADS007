package domain;

public class Leitura {

	private int ano;
	private int mes;
	private int dia;
	private double valor;
	private String uni;
	
	
	public Leitura(int ano, int mes, int dia, double valor, String uni) {
		
		this.ano = ano;
		this.mes = mes;
		this.dia = dia;
		this.valor = valor;
		this.uni =uni;
	}
}
